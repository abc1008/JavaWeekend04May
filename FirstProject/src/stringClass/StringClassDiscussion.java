package stringClass;

public class StringClassDiscussion
{
	
	
	
	public static void main(String[] args)
	{
		
		// Syntax 1:
		String s1 = "abc";   // string literal
		System.out.println(s1);
		
		String s2 = "pqr";   // string literal
		
		String s3 = "pqr";  
		
		
		// Syntax 2:
		String s4 = new String("xyz");   // not preferred
		System.out.println(s4);
		String s5 = new String("xyz"); 
		
	}
	
	

}
